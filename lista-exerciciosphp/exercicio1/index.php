<h2>Apresentação do Curso:</h2>

O curso Técnico em Informática tem por objetivo a formação de profissionais em nível médio para atuar no mercado de trabalho nas diversas áreas de informática, com especificidade no desenvolvimento de sistemas computacionais.

Conforme a proposta educacional da instituição objetiva-se também uma formação humanística e integral para que além de técnicos, os profissionais sejam cidadãos críticos e reflexivos capazes de compreender e atuar em sua realidade, explorando o uso das tecnologias com responsabilidade social.

<h2>Objetivos:</h2>

Desenvolver e operar sistemas, aplicações e interfaces gráficas;

Organizar a coleta e documentação de informações sobre o desenvolvimento de projetos;

Desenvolver programas de computadores, integrado à tecnologia de banco de dados, rede de computadores e web;

Aplicar linguagens e ambientes de programação no desenvolvimento de software;

Selecionar programas de aplicação a partir da avaliação das necessidades do usuário;

Identificar o funcionamento e relacionamento entre os componentes de computadores e seus periféricos;Identificar os serviços de sistemas operacionais e de rede.
